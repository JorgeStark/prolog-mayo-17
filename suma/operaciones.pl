%Suma
suma(Numero1,Numero2,Resultado):- Resultado is Numero1 + Numero2.

%Resta
resta(Numero1,Numero2,Resultado):- Resultado is Numero1 - Numero2. 

%Multiplicacion
multiplicar(Numero1,Numero2,Resultado):-Resultado is Numero1 * Numero2.

%Division
dividir(Numero1,Numero2,Resultado):- Resultado is Numero1 / Numero2.